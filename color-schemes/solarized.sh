# Solarized colorscheme

# Solarized https://ethanschoonover.com/solarized/ is carefully designed to be
# readable by separating colors into three categories: Background, Content, and
# Accent.  So here we do not define background codes for content or accent
# colors.  People who make fancy prompts with accent colors as backgrounds have
# no respect you and think you will sacrifice your eyesight for some candy.

# Background colors
COL_BG_BASE03="$(tput setab 8)"
COL_FG_BASE01="$(tput setaf 10)"
COL_BG_BASE02="$(tput setab 0)"
COL_FG_BASE00="$(tput setaf 11)"
# Content colors
COL_BG_BASE2="$(tput setab 7)"
COL_FG_BASE0="$(tput setaf 12)"
COL_BG_BASE3="$(tput setab 15)"
COL_FG_BASE1="$(tput setaf 14)"
# Accent colors
COL_FG_YELLOW="$(tput setaf 3)"
COL_FG_ORANGE="$(tput setaf 9)"
COL_FG_RED="$(tput setaf 1)"
COL_FG_MAGENTA="$(tput setaf 5)"
COL_FG_VIOLET="$(tput setaf 13)"
COL_FG_BLUE="$(tput setaf 4)"
COL_FG_CYAN="$(tput setaf 6)"
COL_FG_GREEN="$(tput setaf 2)"

COL_X_BOLD="$(tput bold)"
COL_X_DIM="$(tput dim)"
COL_X_REVERSE="$(tput rev)"

COL_RESET="$(tput sgr0)"
